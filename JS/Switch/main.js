let dia = 1;

switch (dia) {
    case 1:
        console.log ("lunes 1")
        break;
    case 2:
        console.log ("marets 2")
        break;
    case 3:
        console.log ("miercoles 3")
        break;
    case 4:
        console.log ("jueves 4")
        break;
    case 5:
        console.log ("viernes 5")
        break;
    case 6:
        console.log ("sabado 6")
        break;
    default:
        console.log ("no tenemos dias")
        break;
}

